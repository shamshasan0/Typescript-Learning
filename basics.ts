// add two numbers
function add(a: number, b: number) {
    return a + b;
}

const sum = add(2, 2);

console.log(sum) // 4


// multiply two numbers
function multiply(a: number, b: number) {
    return a * b;
}

const product = multiply(3, 3);

console.log(product); // 9

// divide two numbers
function divide(a: number, b: number) {
    return b / a;
}
const quotient = divide(3, 127);

console.log(quotient.toFixed(1)); // round to one decimal place; 42.3





